async function seed() {
  try {
    await mongoose.connect(process.env.MONGO_URI);
    console.log('✅ Connected to MongoDB');

    // Clear existing data
    await Client.deleteMany({});
    console.log('Cleared existing clients');

    const total = 1157;
    const startYear = 2013;
    const endYear = 2026;

    const cases = [];

    // Generate raw cases (without case numbers)
    for (let i = 0; i < total; i++) {
      const year = Math.floor(Math.random() * (endYear - startYear + 1)) + startYear;
      const caseType = randomItem(caseTypes);
      const name = generateName();
      const phone = phoneNumbers[i % phoneNumbers.length];
      const status = generateStatus(year);
      const town = randomItem(towns);
      const description = generateCaseDescription(caseType, year, status, town);
      const email = ''; // leave blank

      // Set createdAt to a random date within the case year
      const start = new Date(year, 0, 1);
      const end = new Date(year, 11, 31);
      const createdAt = new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
      const updatedAt = createdAt;

      cases.push({
        name,
        year,
        phone,
        description,
        email,
        status,
        createdAt,
        updatedAt
      });
    }

    // Sort by year DESCENDING (newest first)
    cases.sort((a, b) => b.year - a.year);

    // Assign sequential case numbers: AKM-001-2026 (newest) ... AKM-1157-2013 (oldest)
    const clients = [];
    for (let idx = 0; idx < cases.length; idx++) {
      const c = cases[idx];
      const seq = String(idx + 1).padStart(3, '0');
      const caseNumber = `AKM-${seq}-${c.year}`;
      clients.push({
        name: c.name,
        caseNumber,
        caseDescription: c.description,
        phone: c.phone,
        email: c.email,
        status: c.status,
        createdAt: c.createdAt,
        updatedAt: c.updatedAt
      });
    }

    // Insert in batches
    const batchSize = 100;
    for (let i = 0; i < clients.length; i += batchSize) {
      const batch = clients.slice(i, i + batchSize);
      await Client.insertMany(batch);
      console.log(`Inserted ${i + batch.length} of ${total}`);
    }

    console.log('✅ Seeding complete!');
  } catch (err) {
    console.error('Seeding error:', err);
  } finally {
    await mongoose.disconnect();
  }
}

seed();